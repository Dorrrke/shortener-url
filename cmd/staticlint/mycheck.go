// Пакет с мультичекером для проведения статического анализа кода.
// При запуске проверяет файл или все вайлы при промощи следующих анализаторов:
//
// 1. Всех стандартых анализаторов пакета golang.org/x/tools/go/analysis/passes.
//
// 2. Всех анализаторов класса SA пакета staticcheck.io.
//
// 3. Анализатором S1005 пакета staticcheck.io.
//
// 4. Анализатором СТ1003 пакета staticcheck.io.
//
// 5. Анализатором maligned.
//
// 6. Анализатором errwrap.
//
// 7. Анализатором ExitAnalyzer который проверяет используется ли os.Exit в main пакете.
